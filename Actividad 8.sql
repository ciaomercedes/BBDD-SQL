---CREACION DE VISTA: VW_AUM_MEDICO_X_CARGO

CREATE OR REPLACE VIEW VW_AUM_MEDICO_X_CARGO AS
SELECT
    LPAD(REPLACE(TO_CHAR(m.rut_med,'9,999,999'),',','.'),15) || '-' || m.dv_run AS RUT_MEDICO,
    c.descripcion AS CARGO,
    LPAD(REPLACE(TO_CHAR(NVL(m.sueldo_base,0),'99,999,999'),',','.'),25) AS SUELDO_ACTUAL,
    ROUND(NVL(m.sueldo_base,0) * 1.15) AS SUELDO_AUMENTADO

FROM syn_med m
JOIN syn_cargo c
    ON m.cargo_id = c.cargo_id

WHERE UPPER(c.descripcion) LIKE '%ATENCIÓN%';

CREATE INDEX IDX_CARGO_UPPER_DESC
ON syn_cargo (UPPER(descripcion));

SELECT * FROM VW_AUM_MEDICO_X_CARGO
ORDER BY SUELDO_AUMENTADO DESC;

