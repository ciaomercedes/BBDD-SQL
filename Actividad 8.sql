-- CREACION DE VISTA: VW_RECALCULO_COSTOS
CREATE OR REPLACE VIEW VW_RECALCULO_COSTOS AS
SELECT
    LPAD(REPLACE(TO_CHAR(pac.pac_run,'99,999,999'),',','.'),15) || '-' || pac.dv_run AS RUT_PACIENTE,
    UPPER(pac.apaterno || ' ' || pac.amaterno || ' ' || pac.pnombre) AS NOMBRE_PACIENTE,
    NVL(INITCAP(sld.descripcion),'Sin sistema de salud') AS SISTEMA_SALUD,
    LPAD(REPLACE(TO_CHAR(bc.costo, '$99,999'),',','.'),25) AS COSTO,
    bc.hr_consulta AS HORARIO_ATENCION,
    TO_CHAR(bc.fecha_bono, 'MM-YYYY') AS FECHA_CONSULTA,
    
    CASE 
        WHEN bc.costo BETWEEN 15000 AND 25000 THEN ROUND(bc.costo * 1.15)
        WHEN bc.costo > 25000 THEN ROUND(bc.costo * 1.20)
        ELSE bc.costo
    END AS REAJUSTE

FROM syn_bc bc
JOIN syn_pac pac
    ON pac.pac_run = bc.pac_run
JOIN syn_sld sld
    ON pac.sal_id = sld.tipo_sal_id
    
WHERE EXTRACT(YEAR FROM bc.fecha_bono) = EXTRACT(YEAR FROM SYSDATE) - 1
AND TO_DATE(bc.hr_consulta, 'HH24:mi') > TO_DATE ('17:15', 'HH24:mi')
AND sld.sal_id IN (
                   SELECT sal_id
                   FROM syn_sld
                   WHERE sal_id BETWEEN 10 AND 40 ---PARA FONASA
                   OR sal_id BETWEEN 50 AND 100 ---PARA ISAPRE
                    );

SELECT * FROM VW_RECALCULO_COSTOS
ORDER BY FECHA_CONSULTA DESC, RUT_PACIENTE ASC;

GRANT SELECT ON PRY2205_USER2.VW_RECALCULO_COSTOS TO PRY2205_USER1;

